    <h4>Análisis de Deformación Área-Profundidad</h4>
<p>El análisis de deformación área-profundidad es una forma valiosa de estudiar las estructuras que sobresalen en un desprendimiento subyacente paralelo a la estratigrafía regional (Método profundidad a desprendimiento / Depth-detachment Method), o se forman por encima de una falla que atraviesa el lecho profundo (Método de trayectoria de falla / Fault Trajectory Method).</p>
<p>La metodología funciona analizando la variación del exceso de área estructural con la profundidad. Con las circunstancias apropiadas los resultados son:</p>
<ul>
    <li>La determinación de la profundidad de desprendimiento de control, o de la ubicación y buzamiento de la falla de control. </li>
    <li>Estimación del desplazamiento a lo largo del desprendimiento o falla de control para los horizontes de crecimiento y pre-crecimiento.</li>
    <li>Estimación de la deformación paralela a las capas para todos los horizontes.</li>
</ul>

